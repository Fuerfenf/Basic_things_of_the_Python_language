#-> >
# greater than
print(2 > 1) # True

#-> <
# less than
print(2 < 1) # False

#-> ==
# equal
print(2 == 2) # True
print(1 == 2) # False

#-> !=
# not equal
print(1 != 2) # True
print(2 != 2) # False

#-> >=
# greater than or equal
print(2 >= 1) # True
print(2 >= 2) # True
print(2 >= 3) # False

#-> <=
# less than or equal
print(2 <= 1) # False
print(2 <= 2) # True
print(2 <= 3) # True
